package br.com.maury.clinicaCristiano.fluxoDeCaixa.modelo;

public class PortadorSaldo {
	private Double saldoDisponivelInicial;
	private Double saldoBrutoInicial;
	private Double saldoDisponivelDataConciliacao;
	private Double saldoBrutoDataConciliacao;
	private Double saldoDisponivelAtual;
	private Double saldoBrutoAtual;

	public Double getSaldoDisponivelInicial() {
		return saldoDisponivelInicial;
	}

	public void setSaldoDisponivelInicial(Double saldoDisponivelInicial) {
		this.saldoDisponivelInicial = saldoDisponivelInicial;
	}

	public Double getSaldoBrutoInicial() {
		return saldoBrutoInicial;
	}

	public void setSaldoBrutoInicial(Double saldoBrutoInicial) {
		this.saldoBrutoInicial = saldoBrutoInicial;
	}

	public Double getSaldoDisponivelDataConciliacao() {
		return saldoDisponivelDataConciliacao;
	}

	public void setSaldoDisponivelDataConciliacao(Double saldoDisponivelDataConciliacao) {
		this.saldoDisponivelDataConciliacao = saldoDisponivelDataConciliacao;
	}

	public Double getSaldoBrutoDataConciliacao() {
		return saldoBrutoDataConciliacao;
	}

	public void setSaldoBrutoDataConciliacao(Double saldoBrutoDataConciliacao) {
		this.saldoBrutoDataConciliacao = saldoBrutoDataConciliacao;
	}

	public Double getSaldoDisponivelAtual() {
		return saldoDisponivelAtual;
	}

	public void setSaldoDisponivelAtual(Double saldoDisponivelAtual) {
		this.saldoDisponivelAtual = saldoDisponivelAtual;
	}

	public Double getSaldoBrutoAtual() {
		return saldoBrutoAtual;
	}

	public void setSaldoBrutoAtual(Double saldoBrutoAtual) {
		this.saldoBrutoAtual = saldoBrutoAtual;
	}
}
